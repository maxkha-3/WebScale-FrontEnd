    getAllTasks = (): Promise<any> => {
        return new Promise((resolve, reject) => {

            let data = [].concat.apply([], this.overviewData.map(x => ( x.tasks.map( y => ({task_id: y.task_id, timeline: y.timeline})))));
            resolve(data);
        });
    };

    getTasksInMonitor = (id: string): Promise<any> => {
        return new Promise((resolve, reject) => {

            let monitor = this.overviewData.find(x => x.monitor_id == id);
            let data = monitor.tasks.map(x => ({task_id: x.task_id, timeline: x.timeline}));
            resolve(data);
        });
    };

    getAllStreams = (): Promise<any> => {
        return new Promise((resolve, reject) => {

            let data = [].concat.apply([], [].concat.apply([], this.overviewData.map(x => ( x.tasks.map( y => ( y.streams.map( z => ({stream_id: z.stream_id, timeline: z.timeline}))))))));
            resolve(data);
        });
    };

    getStreamsInTask = (id: string): Promise<any> => {
        return new Promise((resolve, reject) => {

            let task = [].concat.apply([], this.overviewData.map(x => x.tasks)).find(y => y.task_id == id)
            let data = task.streams.map(x => ({stream_id: x.stream_id, timeline: x.timeline}));
            resolve(data);
        });
    };
